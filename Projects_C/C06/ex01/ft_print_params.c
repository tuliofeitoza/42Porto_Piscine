#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	f;

	f = 1;
	while (f < ac)
	{
		i = 0;
		while (av[f][i] != '\0')
		{
			write(1, &av[f][i], 1);
			i++;
		}
		f++;
		write(1, &"\n", 1);
	}
}
